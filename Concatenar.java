public class Concatenar {

    public static void main(String[] args) { 
        int resultado = 5;
        System.out.println("Ejemplos de salidas por pantalla");
        System.out.println("El resultado es " + resultado + 5);
        System.out.println("El resultado es " + (resultado + 5));
        System.out.println("El resultado es " + resultado + "5");
        System.out.print("El resultado es " + resultado + 5);
    }

}
